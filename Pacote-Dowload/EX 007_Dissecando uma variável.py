a=input('Digite algo aqui:')
print('A classificação disso é de:' , type(a))
print('O item digitado é um número:' , a.isnumeric())
print('So tem espaços::' , a.isspace())
print('É alfabetico?' , a.isalpha())
print('É alfanúmerico?' , a.isalnum())
print('Esta capitalizada?' , a.istitle())
print('É imprimível?' , a.isprintable())
print('É composto apenas por decimais?' , a.isdecimal())
print('É composto apenas por dígitos?' , a.isdigit())
print('É um indentificador de código?' , a.isidentifier())
print('É composto apenas por letras minúsculas?' , a.islower())
print('É composto apenas por letras maiúsculas?' , a.isupper())






